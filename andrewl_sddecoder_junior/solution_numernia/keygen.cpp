QWORD Z2Z_Div(QWORD dividend, QWORD divisor, QWORD *remainder)
{
    QWORD quotient = 0;
    int degree = 63;
    while(!(divisor & (QWORD)0x8000000000000000)){
        divisor <<= 1;
        degree--;
    }
    quotient = 0;
    for(int bit = 63; bit >= degree; --bit){
        if(dividend & ((QWORD)1<<bit)){
            quotient |= ((QWORD)1<<(bit-degree));
			dividend ^= divisor;
        }
		divisor >>= (QWORD)1;
    }
	*remainder = dividend;
	return quotient;
}
QWORD Z2Z_Mul(QWORD a, QWORD b) {
	QWORD p = 0;
	QWORD counter;
	for(counter = 0; counter < 64; counter++) {
		if(b & 1) 
			p ^= a;
		a <<= 1;
		b >>= 1;
	}
	return p;
}
QWORD Z2Z_Add(QWORD a, QWORD b) {
	return (a ^ b);
}
