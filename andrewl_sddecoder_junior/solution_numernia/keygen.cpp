/*
INPUT: A matrix a, with RADERxRADER equations over GF(2), answer vector is at pos RADER(last)
OUTPUT: The identity matrix, answer vector located at last position([RADER])
*/
QWORD GaussEliminationGF2(int a[100][100], QWORD V[65], QWORD y)
{
	int s=0, i=0, j=0, k=0, p=0, temp=0;
	QWORD tp;
	/* Init phase, zero memory */
	for (i = 0; i < 100; i++){
		for (j = 0; j < 100; j++){
			a[i][j] = 0;
		}
	}

	/* Build table, convert V to binary and store in matrix */
	for (i = 0; i < RADER; i++)
	{
		tp = V[i];
		for (j = 0; j < RADER; j++)
		{
			a[i][63-j] = tp & 1;
			tp >>= 1;
		}
	}
	tp = y; //This is the answer matrix
   
	for (j = 0; j < RADER; j++)
	{
		a[63-j][RADER] = tp & 1;
		tp >>= 1;
	}

  
	/* Begin Gauss Elimination over GF(2)*/
	for (k = 0; k < KOLS; k++)
	{
		s = k;
		while(a[s][k] == 0 && s <= KOLS){
			s++;}
		for (p = 0; p < KOLS; p++){ //exchange rows
			temp = a[k][p];
			a[k][p] = a[s][p];
			a[s][p] = temp;
		}
		for (i = 0; i < RADER; i++){
			if( (i != k) && (a[i][k] == 1) ){
				for (j = k-1; j < KOLS; j++){
					a[i][j] = a[i][j] ^ a[k][j]; //
				}
			}
		}
	}
	/* Build the X(answer) vector to a number for returning. */
	tp = 0;
	for (p = 0; p < RADER; p++){
		if(a[p][KOLS-1]==1){
			tp |= (QWORD)1<<(63-p);
		}

	   
	}
   return tp;
}
