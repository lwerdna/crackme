/*f_evaluate bruteforce, very simple */
QWORD EvaluateVal(QWORD Y)
{
	QWORD bp = Y, bi = 0, bt[3]={0}, ax=0;
	for (QWORD i = 0; i < 0xFFFF; i++) // high bits 1
	{
		bi = i<<48;
		ax = f_evaluate2(bi,16);
		if((ax & 0xFFFF000000000000) == (bp & 0xFFFF000000000000)) 
		{
			for (QWORD j = 0; j < 0xFFFF; j++) // 2
			{
				bt[0] = bi + (j << 32);
				ax = f_evaluate2(bt[0],32);
				if((ax & 0xFFFFFFFF00000000) == (bp & 0xFFFFFFFF00000000))
				{
					for (QWORD k = 0; k < 0xFFFF; k++) // 3
					{
						bt[1] = bt[0] + (k << 16);
						ax = f_evaluate2(bt[1],48);

						if((ax & 0xFFFFFFFFFFFF0000) == (bp & 0xFFFFFFFFFFFF0000))
						{
							for (QWORD p = 0; p < 0xFFFF; p++)
							{
								bt[2] = bt[1] + p;
								ax = f_evaluate2(bt[2],64);
								if(ax == bp)
								{
									return bt[2];
								}
							
							}
						}
					}
				}
			}
		}
	}
	return 0;
}


